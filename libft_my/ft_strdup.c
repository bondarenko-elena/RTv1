#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*new_str;

	i = 0;
	while (s[i] != '\0')
	{
		i++;
	}
	new_str = (char *)malloc(sizeof(char) * (i + 1));
	if (new_str == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		new_str[i] = s[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
